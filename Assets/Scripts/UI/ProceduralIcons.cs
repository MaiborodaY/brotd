    private static Texture2D Build(int size, byte[] pixels, Color32[] palette)
    {
        var tex = new Texture2D(size, size, TextureFormat.RGBA32, false);
        tex.filterMode = FilterMode.Point; // пиксельный стиль без сглаживания

        var colors = new Color32[size * size];
        for (int i = 0; i < pixels.Length && i < colors.Length; i++)
        {
            int x = i % size;
            int y = size - 1 - (i / size); // Unity: Y снизу вверх
            colors[y * size + x] = palette[pixels[i]];
        }

        tex.SetPixels32(colors);
        tex.Apply();
        return tex;
    }
