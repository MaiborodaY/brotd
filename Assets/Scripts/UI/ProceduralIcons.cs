    private static Texture2D BuildSkull(Color32 mainColor)
    {
        int size = 32;
        var tex  = new Texture2D(size, size, TextureFormat.RGBA32, false);
        tex.filterMode = FilterMode.Point;

        var pixels = new Color32[size * size];
        // Прозрачный фон
        for (int i = 0; i < pixels.Length; i++)
            pixels[i] = new Color32(0, 0, 0, 0);

        Color32 dark    = new Color32((byte)(mainColor.r / 2), (byte)(mainColor.g / 2), (byte)(mainColor.b / 2), 255);
        Color32 white   = new Color32(240, 240, 240, 255);
        Color32 black   = new Color32(20,  20,  20,  255);

        // Вспомогательная функция: закрасить пиксель (x,y) — Y снизу
        void Set(int x, int y, Color32 c)
        {
            if (x < 0 || x >= size || y < 0 || y >= size) return;
            pixels[y * size + x] = c;
        }
        void Rect(int x, int y, int w, int h, Color32 c)
        {
            for (int dy = 0; dy < h; dy++)
                for (int dx = 0; dx < w; dx++)
                    Set(x + dx, y + dy, c);
        }
        void Circle(int cx, int cy, int r, Color32 c)
        {
            for (int dy = -r; dy <= r; dy++)
                for (int dx = -r; dx <= r; dx++)
                    if (dx * dx + dy * dy <= r * r)
                        Set(cx + dx, cy + dy, c);
        }

        // Череп — круглая голова
        Circle(16, 20, 9, dark);
        Circle(16, 20, 8, mainColor);

        // Глаза (чёрные дыры)
        Circle(12, 21, 3, black);
        Circle(20, 21, 3, black);

        // Нос (маленький треугольник)
        Set(16, 17, black);
        Set(15, 16, black);
        Set(17, 16, black);

        // Зубы
        Rect(11, 12, 3, 2, white);
        Rect(15, 12, 3, 2, white);
        Rect(19, 12, 3, 2, white);
        // Линии между зубами
        Set(14, 12, black); Set(14, 13, black);
        Set(18, 12, black); Set(18, 13, black);
        // Нижняя челюсть
        Rect(10, 11, 13, 2, mainColor);
        // Контур челюсти
        for (int x = 10; x < 23; x++) Set(x, 10, dark);

        tex.SetPixels32(pixels);
        tex.Apply();
        return tex;
    }

